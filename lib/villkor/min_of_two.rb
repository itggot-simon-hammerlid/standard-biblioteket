# Public: Compares two numbers, returns smallest number.
#
# num1 - Number that will be compared.
# num2 - Number that will be compared.
#
# Examples
#
#   min_of_two(2,5)
#   # => 2
#
# Returns the smaller number.
def min_of_two(num1, num2)
    if num1 >= num2
        output = num2
    else output = num1
    end
    return output
end

        
    
